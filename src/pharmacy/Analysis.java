    private void incomeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_incomeBtnActionPerformed
        // TODO add your handling code here:
        String fromDate = fromDateTxt2.getText();
        String toDate = toDateTxt2.getText();
        try{
                model1.setRowCount(0);
                query = "SELECT * FROM retail_invoice WHERE "
                        + "date BETWEEN '" + fromDate + "' AND '" + toDate + "';";
                rs = st.executeQuery(query);
                while(rs.next()){
                    int bno = rs.getInt("bill_no");
                    int cid = rs.getInt("cid");
                    int eid = rs.getInt("eid");
                    int amount = rs.getInt("amount");
                    String date = rs.getString("date");
                    model1.addRow(new Object[]{bno,cid,eid,amount,date});
                    }
                query = "SELECT sum(amount) FROM retail_invoice WHERE "
                        + "date BETWEEN '" + fromDate + "' AND '" + toDate + "';";
                rs = st.executeQuery(query);
                rs.next();
                jLabel1.setText("Total Income(From Customers): â‚¹" + rs.getInt(1));
            }
            catch(Exception e){
                        JOptionPane.showMessageDialog(null,e);
                }
    }//GEN-LAST:event_incomeBtnActionPerformed

